# ################### 初始化 ##########################
a0 = [0 for _ in range(5)]
a0[0] += 1
print(a0)

a1 = [0] * 5
a1[0] += 1
print(a1)
# #####################################################
a2 = [[0] for _ in range(5)]
a2[0][0] += 1
print(a2)

a3 = [[0]] * 5
print(a3)
a3[0][0] += 1
print(type(a3), a3)


# #################### 列表拷贝 #######################
b = [1, 2, 3, 4, 5]
print("父向量a：", b)
c = b.copy()
print("子向量c：", c)
c[0] += 9
print("子向量自增后父向量c：", b)
print("子向量自增后子向量c：", c)
